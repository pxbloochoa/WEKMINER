    private void buttonAlgoritmoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAlgoritmoMouseClicked
        // TODO add your handling code here:
        int b=Integer.parseInt(noClosterTextFiled.getText());
        path=rutaTextField.getText();
        if(b!=0){           
            Cluster clus=new Cluster(tablaETL);        
            try {
                clus.concatena(jTextArea1,b,path);
            } catch (Exception ex) {
                Logger.getLogger(prueba.class.getName()).log(Level.SEVERE, null, ex);
            }        
            // Escribir(); 
            String saludo=jTextArea1.getText();      
            try{  
            //Crear un objeto File se encarga de crear o abrir acceso a un archivo que se especifica en su constructor
                File archivo=new File("texto.txt");
                BorrarFichero(archivo); 
                //Crear objeto FileWriter que sera el que nos ayude a escribir sobre archivo
                FileWriter escribir=new FileWriter(archivo,true);
                //Escribimos en el archivo con el metodo write 
                escribir.write(saludo);
                //Cerramos la conexion
                escribir.close();
                
                jTabbedPane1.setEnabledAt(2, true);
                jPanel5.removeAll();
            }
            //Si existe un problema al escribir cae aqui
            catch(Exception e)
            {
                System.out.println("Error al escribir");
            }
        }
         
    }//GEN-LAST:event_buttonAlgoritmoMouseClicked

