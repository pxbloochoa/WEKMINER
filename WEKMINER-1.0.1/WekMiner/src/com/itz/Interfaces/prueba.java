    public static void obtieneArchivo(){
        File fichero;
        String ruta="";
        int resultado;
        FileChooser ventana = new FileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("CSV","csv");
        ventana.fcCSV.setFileFilter(filtro);
        resultado= ventana.fcCSV.showOpenDialog(null);
        if (JFileChooser.APPROVE_OPTION == resultado){
            fichero = ventana.fcCSV.getSelectedFile();
            ruta =ventana.fcCSV.getSelectedFile().getAbsolutePath();
        //JOptionPane.showMessageDialog(this, ruta);
                         
        }
        rutaTextField.setText(ruta);
  //labelRuta.setText(ruta);
    }
    static void llenaTabla(){
        ruta=rutaTextField.getText();
        if(ruta.equals("")==false){
            objetoCSV.arregloCSV(tablaETL, ruta);
        }
        
    }
    
    public static  void BorrarFichero(File Ffichero){
        try {
         /*Si existe el fichero*/
            if(Ffichero.exists()){
                /*Borra el fichero*/  
                Ffichero.delete(); 
                System.out.println("Fichero Borrado con Exito");
            }
        } catch (Exception ex) {
         /*Captura un posible error y le imprime en pantalla*/ 
          System.out.println(ex.getMessage());
        }
    }


